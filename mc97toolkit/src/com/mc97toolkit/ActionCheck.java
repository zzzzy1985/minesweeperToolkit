package com.mc97toolkit;

public class ActionCheck {
	public String timeStamp;
	public String check;
	public int x = 0;
	public int y = 0;

	
	public ActionCheck(String time, String check, int x, int y) {
		this.timeStamp = time;
		this.check = check;
		this.x = x;
		this.y = y;
	
	}
}
